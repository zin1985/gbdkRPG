/* rpg128/rpg149: draw battle enemies as BG tiles instead of OBJ sprites.
 *
 * rpg149 adds formation sizes:
 *   S = 16x16, up to 6 bodies
 *   M = 32x32, up to 3 bodies
 *   L = 96x32, one large body
 *
 * Enemy BG tiles must stay inside 80-127 because the Japanese font cache uses
 * 128-223.  rpg152 therefore loads only the current formation art into the
 * safe 48-tile window and remaps sprite_kind values to runtime slots 0-2.
 * S-size kind 0 is now the user-provided 16x16 "ビット" sprite.
 * L-size golem uses all 48 safe tiles by itself.
 */
static const unsigned char *battle_enemy_tile_source(UINT8 kind) {
    if (kind == 0u) return battle_enemy_tiles_bit;
    if (kind == 3u) return battle_enemy_tiles_warmachine;
    if (kind == 6u) return battle_enemy_tiles_mimic;
    if (kind == 7u) return battle_enemy_tiles_skeleton;
    if (kind == 8u) return battle_enemy_tiles_plant_eye;
    if (kind == 9u) return battle_enemy_tiles_plant_gaze;
    if (kind > 2u) kind = 0u;
    return &battle_enemy_tiles[(UINT16)kind * 16u * 16u];
}

static UINT8 battle_runtime_art_slot_map[10];

static void battle_runtime_art_map_clear(void) {
    UINT8 i;
    for (i = 0u; i < 10u; i++) battle_runtime_art_slot_map[i] = 0u;
}

static UINT8 battle_runtime_art_mapped_slot(UINT8 kind) {
    if (kind >= 10u) return 0u;
    return battle_runtime_art_slot_map[kind];
}

void battle_enemy_bg_load_tiles(void) BANKED {
    set_bkg_data(BATTLE_ENEMY_BG_TILE_BASE, 48u, battle_enemy_tiles);
}

static void battle_enemy_bg_load_golem_l_bank1(void) {
    VBK_REG = 1u;
    set_bkg_data(BATTLE_ENEMY_BG_TILE_BASE, 90u, battle_enemy_tiles_golem_l);
    VBK_REG = VBK_TILES;
}

static UINT8 battle_runtime_art_slot(UINT8 kind, UINT8 *unique_kinds, UINT8 *unique_count) {
    UINT8 i;

    for (i = 0u; i < *unique_count; i++) {
        if (unique_kinds[i] == kind) return i;
    }

    if (*unique_count >= 3u) return 0u;

    unique_kinds[*unique_count] = kind;
    *unique_count = (UINT8)(*unique_count + 1u);
    return (UINT8)(*unique_count - 1u);
}

static void battle_enemy_overlay_load_tiles_for_formation(UINT8 count, const UINT8 *sprite_kinds, const UINT8 *size_kinds);

void battle_enemy_bg_load_tiles_for_formation(UINT8 count, UINT8 *sprite_kinds, const UINT8 *size_kinds) BANKED {
    UINT8 i;
    UINT8 slot;
    UINT8 has_l;
    UINT8 unique_count;
    UINT8 unique_kinds[3];

    if (count > 6u) count = 6u;

    has_l = 0u;
    for (i = 0u; i < count; i++) {
        if (size_kinds[i] == BATTLE_ENEMY_SIZE_L) {
            has_l = 1u;
            break;
        }
    }

    if (has_l) {
        UINT8 l_slot = 0u;
        for (i = 0u; i < count; i++) {
            if (size_kinds[i] == BATTLE_ENEMY_SIZE_L) { l_slot = i; break; }
        }
        /* Dedicated L-size body: use the entire safe enemy tile window. */
        if (sprite_kinds[l_slot] == 5u) {
            set_bkg_data(BATTLE_ENEMY_BG_TILE_BASE, 48u, battle_enemy_tiles_warmachine_l);
        } else {
            battle_enemy_bg_load_golem_l_bank1();
        }
        /* rpg291: do not remap original L art ids here.  L drawing ignores
         * sprite_kind and always uses the loaded 96x32 tile body; preserving
         * the id keeps item-menu return from selecting the wrong L art.
         */
        return;
    }

    unique_count = 0u;
    unique_kinds[0] = 0u;
    unique_kinds[1] = 0u;
    unique_kinds[2] = 0u;

    battle_runtime_art_map_clear();
    for (i = 0u; i < count; i++) {
        slot = battle_runtime_art_slot(sprite_kinds[i], unique_kinds, &unique_count);
        if (sprite_kinds[i] < 10u) battle_runtime_art_slot_map[sprite_kinds[i]] = slot;
    }

    for (i = 0u; i < unique_count; i++) {
        set_bkg_data((UINT8)(BATTLE_ENEMY_BG_TILE_BASE + (UINT8)(i << 4)),
                     16u,
                     battle_enemy_tile_source(unique_kinds[i]));
    }
    battle_enemy_overlay_load_tiles_for_formation(count, sprite_kinds, size_kinds);
}

