/* rpg128/rpg149: draw battle enemies as BG tiles instead of OBJ sprites.
 *
 * rpg149 adds formation sizes:
 *   S = 16x16, up to 6 bodies
 *   M = 32x32, up to 3 bodies
 *   L = 96x32, one large body
 *
 * Enemy BG tiles must stay inside 80-127 because the Japanese font cache uses
 * 128-223.  rpg152 therefore loads only the current formation art into the
 * safe 48-tile window and remaps sprite_kind values to runtime slots 0-2.
 * S-size kind 0 is now the user-provided 16x16 "ビット" sprite.
 * L-size golem uses all 48 safe tiles by itself.
 */
static const unsigned char *battle_enemy_tile_source(UINT8 kind) {
    if (kind == 0u) return battle_enemy_tiles_bit;
    if (kind == 3u) return battle_enemy_tiles_warmachine;
    if (kind > 2u) kind = 0u;
    return &battle_enemy_tiles[(UINT16)kind * 16u * 16u];
}

void battle_enemy_bg_load_tiles(void) BANKED {
    set_bkg_data(BATTLE_ENEMY_BG_TILE_BASE, 48u, battle_enemy_tiles);
}

static UINT8 battle_runtime_art_slot(UINT8 kind, UINT8 *unique_kinds, UINT8 *unique_count) {
    UINT8 i;

    for (i = 0u; i < *unique_count; i++) {
        if (unique_kinds[i] == kind) return i;
    }

    if (*unique_count >= 3u) return 0u;

    unique_kinds[*unique_count] = kind;
    *unique_count = (UINT8)(*unique_count + 1u);
    return (UINT8)(*unique_count - 1u);
}

void battle_enemy_bg_load_tiles_for_formation(UINT8 count, UINT8 *sprite_kinds, const UINT8 *size_kinds) BANKED {
    UINT8 i;
    UINT8 slot;
    UINT8 has_l;
    UINT8 unique_count;
    UINT8 unique_kinds[3];

    if (count > 6u) count = 6u;

    has_l = 0u;
    for (i = 0u; i < count; i++) {
        if (size_kinds[i] == BATTLE_ENEMY_SIZE_L) {
            has_l = 1u;
            break;
        }
    }

    if (has_l) {
        /* Dedicated L-size golem: use the entire safe enemy tile window. */
        set_bkg_data(BATTLE_ENEMY_BG_TILE_BASE, 48u, battle_enemy_tiles_golem_l);
        for (i = 0u; i < count; i++) sprite_kinds[i] = 0u;
        return;
    }

    unique_count = 0u;
    unique_kinds[0] = 0u;
    unique_kinds[1] = 0u;
    unique_kinds[2] = 0u;

    for (i = 0u; i < count; i++) {
        slot = battle_runtime_art_slot(sprite_kinds[i], unique_kinds, &unique_count);
        sprite_kinds[i] = slot;
    }

    for (i = 0u; i < unique_count; i++) {
        set_bkg_data((UINT8)(BATTLE_ENEMY_BG_TILE_BASE + (UINT8)(i << 4)),
                     16u,
                     battle_enemy_tile_source(unique_kinds[i]));
    }
}

static void battle_enemy_bg_origin_for_slot(UINT8 count, UINT8 slot, UINT8 size_kind, UINT8 *x, UINT8 *y) {
    if (size_kind == 0u) { /* S 16x16, 3 columns x 2 rows */
        *x = (UINT8)(2u + (UINT8)(slot % 3u) * 6u);
        *y = (slot < 3u) ? 5u : 8u;
        return;
    }

    if (size_kind == 2u) { /* L 96x32 */
        (void)count;
        (void)slot;
        *x = 4u;
        *y = 5u;
        return;
    }

    /* M 32x32 */
    if (count <= 1u) {
        *x = 8u;
    } else if (count == 2u) {
        *x = (slot == 0u) ? 4u : 12u;
    } else {
        if (slot == 0u) *x = 1u;
        else if (slot == 1u) *x = 8u;
        else *x = 15u;
    }
    *y = BATTLE_ENEMY_BG_Y;
}

static void battle_enemy_bg_clear_rect(UINT8 x, UINT8 y, UINT8 w, UINT8 h) {
    UINT8 i;
    UINT8 row;
    unsigned char tiles[20];
    UINT8 blank;

    if (w > 20u) w = 20u;
    blank = (UINT8)(JP_FRAME_BASE + 0u);
    for (i = 0u; i < 20u; i++) tiles[i] = blank;

    for (row = 0u; row < h; row++) {
        set_bkg_tiles(x, (UINT8)(y + row), w, 1u, tiles);
    }
}

void battle_enemy_bg_draw_slot_sized(UINT8 count, UINT8 slot, UINT8 sprite_kind, UINT8 size_kind, UINT8 alive) BANKED {
    UINT8 x;
    UINT8 y;
    UINT8 base;
    unsigned char tiles[48];

    if (slot >= 6u) return;
    if (sprite_kind > 2u) sprite_kind = 0u;
    if (size_kind > 2u) size_kind = 1u;

    battle_enemy_bg_origin_for_slot(count, slot, size_kind, &x, &y);

    if (size_kind == 0u) { /* S: 2x2 */
        if (!alive) {
            battle_enemy_bg_clear_rect(x, y, 2u, 2u);
        } else {
            base = (UINT8)(BATTLE_ENEMY_BG_TILE_BASE + (UINT8)(sprite_kind << 4));
            tiles[0] = (UINT8)(base + 0u);
            tiles[1] = (UINT8)(base + 2u);
            tiles[2] = (UINT8)(base + 1u);
            tiles[3] = (UINT8)(base + 3u);
            set_bkg_tiles(x, y, 2u, 2u, tiles);
        }
        return;
    }

    if (size_kind == 2u) { /* L: 96x32 */
        if (!alive) {
            battle_enemy_bg_clear_rect(x, y, 12u, 4u);
        } else {
            UINT8 row;
            base = BATTLE_ENEMY_BG_TILE_BASE;
            for (row = 0u; row < 48u; row++) {
                tiles[row] = (UINT8)(base + row);
            }
            set_bkg_tiles(x, y, 12u, 4u, tiles);
        }
        return;
    }

    /* M: 4x4 */
    if (!alive) {
        battle_enemy_bg_clear_rect(x, y, BATTLE_ENEMY_BG_W, BATTLE_ENEMY_BG_H);
    } else {
        base = (UINT8)(BATTLE_ENEMY_BG_TILE_BASE + (UINT8)(sprite_kind << 4));
        tiles[0]  = (UINT8)(base + 0u);
        tiles[1]  = (UINT8)(base + 2u);
        tiles[2]  = (UINT8)(base + 4u);
        tiles[3]  = (UINT8)(base + 6u);
        tiles[4]  = (UINT8)(base + 1u);
        tiles[5]  = (UINT8)(base + 3u);
        tiles[6]  = (UINT8)(base + 5u);
        tiles[7]  = (UINT8)(base + 7u);
        tiles[8]  = (UINT8)(base + 8u);
        tiles[9]  = (UINT8)(base + 10u);
        tiles[10] = (UINT8)(base + 12u);
        tiles[11] = (UINT8)(base + 14u);
        tiles[12] = (UINT8)(base + 9u);
        tiles[13] = (UINT8)(base + 11u);
        tiles[14] = (UINT8)(base + 13u);
        tiles[15] = (UINT8)(base + 15u);
        set_bkg_tiles(x, y, BATTLE_ENEMY_BG_W, BATTLE_ENEMY_BG_H, tiles);
    }
}

void battle_enemy_bg_draw_slot(UINT8 count, UINT8 slot, UINT8 sprite_kind, UINT8 alive) BANKED {
    battle_enemy_bg_draw_slot_sized(count, slot, sprite_kind, BATTLE_ENEMY_SIZE_M, alive);
}

void battle_enemy_bg_draw_all_sized(UINT8 count, const UINT8 *sprite_kinds, const UINT8 *size_kinds, const UINT8 *alive_flags) BANKED {
    UINT8 i;
    if (count > 6u) count = 6u;

    /* Clear the S/M enemy arena first. */
    battle_enemy_bg_clear_rect(0u, 5u, 20u, 5u);

    for (i = 0u; i < count; i++) {
        battle_enemy_bg_draw_slot_sized(count, i, sprite_kinds[i], size_kinds[i], alive_flags[i]);
    }
}

void battle_enemy_bg_draw_all(UINT8 count, const UINT8 *sprite_kinds, const UINT8 *alive_flags) BANKED {
    UINT8 sizes[3];
    sizes[0] = BATTLE_ENEMY_SIZE_M;
    sizes[1] = BATTLE_ENEMY_SIZE_M;
    sizes[2] = BATTLE_ENEMY_SIZE_M;
    if (count > 3u) count = 3u;
    battle_enemy_bg_draw_all_sized(count, sprite_kinds, sizes, alive_flags);
}
