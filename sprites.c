static void battle_enemy_bg_draw_golem_l_masked(UINT8 x, UINT8 y, UINT8 attr) {
    UINT8 row;
    UINT8 col;
    UINT8 index;
    unsigned char tiles_row[18];
    unsigned char attrs_row[18];
    UINT8 blank;
    const unsigned char *tile_src;

    blank = (UINT8)(JP_FRAME_BASE + 0u);
    index = 0u;
    for (row = 0u; row < 5u; row++) {
        for (col = 0u; col < 18u; col++, index++) {
            tile_src = &battle_enemy_tiles_golem_l[(UINT16)index * 16u];
            if (battle_enemy_bg_tile_is_blank(tile_src)) {
                tiles_row[col] = blank;
                attrs_row[col] = BATTLE_ENEMY_ATTR_DEFAULT;
            } else {
                tiles_row[col] = (UINT8)(BATTLE_ENEMY_BG_TILE_BASE + index);
                attrs_row[col] = attr;
            }
        }
        set_bkg_tiles(x, (UINT8)(y + row), 18u, 1u, tiles_row);
        VBK_REG = VBK_ATTRIBUTES;
        set_bkg_tiles(x, (UINT8)(y + row), 18u, 1u, attrs_row);
        VBK_REG = VBK_TILES;
    }
}

void battle_enemy_bg_draw_slot_sized(UINT8 count, UINT8 slot, UINT8 sprite_kind, UINT8 size_kind, UINT8 alive) BANKED {
    UINT8 x;
    UINT8 y;
    UINT8 base;
    UINT8 original_sprite_kind;
    unsigned char tiles[48];

    if (slot >= 6u) return;
    if (size_kind > 2u) size_kind = 1u;
    original_sprite_kind = sprite_kind;
    if (size_kind != BATTLE_ENEMY_SIZE_L) sprite_kind = battle_runtime_art_mapped_slot(sprite_kind);

    battle_enemy_bg_origin_for_slot(count, slot, size_kind, &x, &y);

    if (size_kind == 0u) { /* S: 2x2 */
        if (!alive) {
            battle_enemy_bg_clear_rect(x, y, 2u, 2u);
        } else {
            base = (UINT8)(BATTLE_ENEMY_BG_TILE_BASE + (UINT8)(sprite_kind << 4));
            tiles[0] = (UINT8)(base + 0u);
            tiles[1] = (UINT8)(base + 2u);
            tiles[2] = (UINT8)(base + 1u);
            tiles[3] = (UINT8)(base + 3u);
            set_bkg_tiles(x, y, 2u, 2u, tiles);
            battle_enemy_bg_set_attr_rect(x, y, 2u, 2u, battle_enemy_bg_attr_for(original_sprite_kind, size_kind));
        }
        return;
    }

    if (size_kind == 2u) { /* L: warmachine 96x32, golem 144x40 via VRAM bank 1 */
        if (sprite_kind == 5u) {
            x = 4u;
            y = BATTLE_ENEMY_BG_Y;
            if (!alive) {
                battle_enemy_bg_clear_rect(x, y, 12u, 4u);
            } else {
                UINT8 row;
                base = BATTLE_ENEMY_BG_TILE_BASE;
                for (row = 0u; row < 48u; row++) {
                    tiles[row] = (UINT8)(base + row);
                }
                set_bkg_tiles(x, y, 12u, 4u, tiles);
                battle_enemy_bg_set_attr_rect(x, y, 12u, 4u, battle_enemy_bg_attr_for(original_sprite_kind, size_kind));
            }
        } else {
            x = 1u;
            y = BATTLE_ENEMY_BG_Y; /* 144x40 giant golem using VRAM bank 1 */
            if (!alive) {
                battle_enemy_bg_clear_rect(x, y, 18u, 5u);
            } else {
                battle_enemy_bg_draw_golem_l_masked(x, y, battle_enemy_bg_attr_for(original_sprite_kind, size_kind));
            }
        }
        return;
    }

    /* M: 4x4 */
    if (!alive) {
        battle_enemy_bg_clear_rect(x, y, BATTLE_ENEMY_BG_W, BATTLE_ENEMY_BG_H);
    } else {
        base = (UINT8)(BATTLE_ENEMY_BG_TILE_BASE + (UINT8)(sprite_kind << 4));
        if (original_sprite_kind == 6u) {
            battle_enemy_bg_draw_mimic_colored(x, y, base);
        } else if (original_sprite_kind == 7u) {
            battle_enemy_bg_draw_skeleton_colored(x, y, base);
        } else if (original_sprite_kind == 8u) {
            battle_enemy_bg_draw_plant_eye_colored(x, y, base);
        } else if (original_sprite_kind == 9u) {
            battle_enemy_bg_draw_plant_gaze_colored(x, y, base);
        } else {
            tiles[0]  = (UINT8)(base + 0u);
            tiles[1]  = (UINT8)(base + 2u);
            tiles[2]  = (UINT8)(base + 4u);
            tiles[3]  = (UINT8)(base + 6u);
            tiles[4]  = (UINT8)(base + 1u);
            tiles[5]  = (UINT8)(base + 3u);
            tiles[6]  = (UINT8)(base + 5u);
            tiles[7]  = (UINT8)(base + 7u);
            tiles[8]  = (UINT8)(base + 8u);
            tiles[9]  = (UINT8)(base + 10u);
            tiles[10] = (UINT8)(base + 12u);
            tiles[11] = (UINT8)(base + 14u);
            tiles[12] = (UINT8)(base + 9u);
            tiles[13] = (UINT8)(base + 11u);
            tiles[14] = (UINT8)(base + 13u);
            tiles[15] = (UINT8)(base + 15u);
            set_bkg_tiles(x, y, BATTLE_ENEMY_BG_W, BATTLE_ENEMY_BG_H, tiles);
            battle_enemy_bg_set_attr_rect(x, y, BATTLE_ENEMY_BG_W, BATTLE_ENEMY_BG_H, battle_enemy_bg_attr_for(original_sprite_kind, size_kind));
        }
    }
}

void battle_enemy_bg_draw_slot(UINT8 count, UINT8 slot, UINT8 sprite_kind, UINT8 alive) BANKED {
    battle_enemy_bg_draw_slot_sized(count, slot, sprite_kind, BATTLE_ENEMY_SIZE_M, alive);
}

void battle_enemy_bg_draw_all_sized(UINT8 count, const UINT8 *sprite_kinds, const UINT8 *size_kinds, const UINT8 *alive_flags) BANKED {
    UINT8 i;
    if (count > 6u) count = 6u;

    /* Clear the full enemy arena first. The giant golem may occupy 18x5 tiles,
     * so reset a generous battle rectangle before any redraw.
     */
    battle_enemy_bg_clear_rect(0u, 5u, 20u, 8u);

    for (i = 0u; i < count; i++) {
        battle_enemy_bg_draw_slot_sized(count, i, sprite_kinds[i], size_kinds[i], alive_flags[i]);
    }
    battle_enemy_overlay_draw_all_sized(count, sprite_kinds, size_kinds, alive_flags);
}

void battle_enemy_bg_draw_all(UINT8 count, const UINT8 *sprite_kinds, const UINT8 *alive_flags) BANKED {
    UINT8 sizes[3];
    sizes[0] = BATTLE_ENEMY_SIZE_M;
    sizes[1] = BATTLE_ENEMY_SIZE_M;
    sizes[2] = BATTLE_ENEMY_SIZE_M;
    if (count > 3u) count = 3u;
    battle_enemy_bg_draw_all_sized(count, sprite_kinds, sizes, alive_flags);
}
