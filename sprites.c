static void battle_enemy_overlay_load_tiles_for_formation(UINT8 count, const UINT8 *sprite_kinds, const UINT8 *size_kinds) {
    UINT8 i;
    UINT8 overlay_kind = 0xFFu;
    UINT8 prop;
    const unsigned char *src = 0;
    static const UINT8 skeleton_props[BATTLE_ENEMY_OVERLAY_SPRITE_COUNT] = {0u,0u,0u,0u,0u,0u,1u,1u};
    const UINT8 *props = skeleton_props;

    for (i = 0u; i < count; i++) {
        if (size_kinds[i] != BATTLE_ENEMY_SIZE_M) continue;
        if (sprite_kinds[i] == 7u) { overlay_kind = 7u; break; }
        if (sprite_kinds[i] == 8u) { overlay_kind = 8u; break; }
        if (sprite_kinds[i] == 9u) { overlay_kind = 9u; break; }
    }

    if (overlay_kind == 7u) {
        src = battle_enemy_overlay_tiles_skeleton;
        props = skeleton_props;
    } else if (overlay_kind == 8u) {
        src = battle_enemy_overlay_tiles_plant_eye;
        props = battle_enemy_obj_prop_plant_eye;
    } else if (overlay_kind == 9u) {
        src = battle_enemy_overlay_tiles_plant_gaze;
        props = battle_enemy_obj_prop_plant_gaze;
    }

    if (src) {
        set_sprite_data(BATTLE_ENEMY_OVERLAY_TILE_BASE, 16u, src);
    }
    for (i = 0u; i < BATTLE_ENEMY_OVERLAY_SPRITE_COUNT; i++) {
        prop = props[i];
        set_sprite_prop((UINT8)(BATTLE_ENEMY_OVERLAY_SPRITE_BASE + i), prop);
        set_sprite_tile((UINT8)(BATTLE_ENEMY_OVERLAY_SPRITE_BASE + i), (UINT8)(BATTLE_ENEMY_OVERLAY_TILE_BASE + (i << 1)));
    }
    battle_enemy_overlay_hide_all();
}

