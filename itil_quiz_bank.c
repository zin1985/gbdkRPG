#define ITIL_QUIZ_COUNT ((UINT8)(sizeof(itil_quizzes) / sizeof(itil_quizzes[0])))

static UINT8 itil_quiz_index(UINT8 idx) BANKED {
    return (UINT8)(idx % ITIL_QUIZ_COUNT);
}

UINT8 itil_quiz_count(void) BANKED {
    return ITIL_QUIZ_COUNT;
}

void itil_quiz_draw(UINT8 idx, UINT8 cursor) BANKED {
    const ItilQuizDef *q;
    q = &itil_quizzes[itil_quiz_index(idx)];

    jp_put_bkg_text(1u, 3u, q->q);
    jp_put_bkg_text(1u, 6u, (cursor == 0u) ? ">" : " ");
    jp_put_bkg_text(3u, 6u, q->a0);
    jp_put_bkg_text(1u, 8u, (cursor == 1u) ? ">" : " ");
    jp_put_bkg_text(3u, 8u, q->a1);
    jp_put_bkg_text(1u, 10u, (cursor == 2u) ? ">" : " ");
    jp_put_bkg_text(3u, 10u, q->a2);
}

UINT8 itil_quiz_is_correct(UINT8 idx, UINT8 cursor) BANKED {
    const ItilQuizDef *q;
    q = &itil_quizzes[itil_quiz_index(idx)];
    return (UINT8)(cursor == q->correct);
}
